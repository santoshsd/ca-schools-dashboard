-- SCHEMA-01: Composite query indexes on performance_data
-- (created CONCURRENTLY so they don't block reads/writes)

CREATE INDEX CONCURRENTLY IF NOT EXISTS idx_perf_school_year_ind
  ON performance_data (school_id, academic_year, indicator_id);

CREATE INDEX CONCURRENTLY IF NOT EXISTS idx_perf_district_year_ind
  ON performance_data (district_id, academic_year, indicator_id);

CREATE INDEX CONCURRENTLY IF NOT EXISTS idx_perf_county_year_ind
  ON performance_data (county_id, academic_year, indicator_id);

CREATE INDEX CONCURRENTLY IF NOT EXISTS idx_perf_ind_group_year
  ON performance_data (indicator_id, student_group_id, academic_year);

-- SCHEMA-03: Expression-based dedupe unique index.
-- Uses COALESCE(x, 0) so NULL values at the same reporting level compare equal.
-- (0 is never a valid serial id so it is safe as a sentinel.)
-- Drop the simple drizzle-managed unique index first (if present from drizzle push),
-- then create the richer expression-based one.
DROP INDEX CONCURRENTLY IF EXISTS idx_perf_dedupe;

CREATE UNIQUE INDEX CONCURRENTLY IF NOT EXISTS idx_perf_dedupe
  ON performance_data (
    COALESCE(school_id,    0),
    COALESCE(district_id,  0),
    COALESCE(county_id,    0),
    indicator_id,
    student_group_id,
    academic_year
  );

-- SCHEMA-05: Partial unique index on users.email (non-null only).
-- PostgreSQL's standard UNIQUE constraint already allows multiple NULLs, so
-- this partial index is semantically equivalent but makes intent explicit.
-- Drop the old inline unique constraint first (name is auto-generated by drizzle
-- as "users_email_unique").
ALTER TABLE users DROP CONSTRAINT IF EXISTS users_email_unique;

CREATE UNIQUE INDEX CONCURRENTLY IF NOT EXISTS idx_users_email_unique
  ON users (email)
  WHERE email IS NOT NULL;

