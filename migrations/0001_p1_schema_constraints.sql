-- SCHEMA-02: CHECK constraint — valid reporting-level combinations.
-- Existing rows must conform before adding the constraint.
-- If this fails, inspect: SELECT * FROM performance_data WHERE NOT (
--   (school_id IS NULL AND district_id IS NULL AND county_id IS NULL) OR
--   (school_id IS NULL AND district_id IS NULL AND county_id IS NOT NULL) OR
--   (school_id IS NULL AND district_id IS NOT NULL AND county_id IS NOT NULL) OR
--   (school_id IS NOT NULL AND district_id IS NOT NULL AND county_id IS NOT NULL)
-- );
ALTER TABLE performance_data
  DROP CONSTRAINT IF EXISTS chk_perf_level_consistency;
ALTER TABLE performance_data
  ADD CONSTRAINT chk_perf_level_consistency CHECK (
    (school_id IS NULL     AND district_id IS NULL     AND county_id IS NULL)     OR
    (school_id IS NULL     AND district_id IS NULL     AND county_id IS NOT NULL) OR
    (school_id IS NULL     AND district_id IS NOT NULL AND county_id IS NOT NULL) OR
    (school_id IS NOT NULL AND district_id IS NOT NULL AND county_id IS NOT NULL)
  );

COMMIT;

